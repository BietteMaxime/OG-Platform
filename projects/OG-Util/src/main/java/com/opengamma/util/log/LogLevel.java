package com.opengamma.util.log;

/**
 * A standard set of logging levels for use with {@link LogEvent}.
 */
public enum LogLevel {

  /**
   * Fatal.
   */
  FATAL,
  /**
   * Error.
   */
  ERROR,
  /**
   * Warning.
   */
  WARN,
  /**
   * Information.
   */
  INFO,
  /**
   * Debug information.
   */
  DEBUG,
  /**
   * Detailed debug information.
   */
  TRACE
  
}
